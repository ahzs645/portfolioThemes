function renderAsciiName(name) {
  const upper = name.toUpperCase();
  const lines = ['', '', '', '', ''];
  for (const ch of upper) {
    const letter = LETTERS[ch] || LETTERS[' '];
    for (let i = 0; i < 5; i++) {
      lines[i] += letter[i] + '  ';
    }
  }
  return lines.join('\n');
}

